    "    \"\"\"\n",
    "    Maximize system reliability subject to budget constraint.\n",
    "    Uses a greedy marginal-gain algorithm suitable for integer allocation.\n",
    "    At each step: add one redundant unit to the component with highest\n",
    "    reliability gain per dollar, until budget exhausted.\n",
    "    \"\"\"\n",
    "    n = len(LAMBDA)\n",
    "    avail = component_availability(LAMBDA, MU)\n",
    "\n",
    "    def subsystem_r(i, r):\n",
    "        \"\"\"Parallel reliability of (1+r) copies of component i.\"\"\"\n",
    "        return 1.0 - (1.0 - avail[i]) ** (1 + r)\n",
    "\n",
    "    def system_r(redun):\n",
    "        return np.prod([subsystem_r(i, r) for i, r in enumerate(redun)])\n",
    "\n",
    "    redun = np.zeros(n, dtype=int)\n",
    "    remaining = budget\n",
    "    baseline = system_r(redun)\n",
    "\n",
    "    for _ in range(budget):   # at most budget/min_cost iterations\n",
    "        best_i, best_gain_per_dollar = -1, 0.0\n",
    "        for i in range(n):\n",
    "            if redun[i] >= max_redundancy:\n",
    "                continue\n",
    "            if costs[i] > remaining:\n",
    "                continue\n",
    "            # Marginal gain from adding one unit to component i\n",
    "            gain = (subsystem_r(i, redun[i] + 1) - subsystem_r(i, redun[i]))\n",
    "            gain_per_dollar = gain / costs[i]\n",
    "            if gain_per_dollar > best_gain_per_dollar:\n",
    "                best_gain_per_dollar = gain_per_dollar\n",
    "                best_i = i\n",
    "        if best_i < 0:\n",
    "            break\n",
    "        redun[best_i] += 1\n",
    "        remaining -= costs[best_i]\n",
    "\n",
    "    return {\n",
    "        \"optimal_redundancy\":     redun,\n",
    "        \"total_cost\":             int(budget - remaining),\n",
    "        \"baseline_reliability\":   round(baseline, 8),\n",
