    "        # Parallel reliability for n_copies of component i\n",
    "        sub_r = 1.0 - (1.0 - avail[i]) ** n_copies\n",
    "        subsystem_reliabilities.append(sub_r)\n",
    "    return np.prod(subsystem_reliabilities)   # series of subsystems\n",
